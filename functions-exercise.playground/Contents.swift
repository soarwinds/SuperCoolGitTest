import UIKit

func add(numberA: Double, numberB: Double) -> Double {
    return (numberA + numberB)
}

func subtract(numberA: Int, numberB: Int) -> Int {
    return numberA - numberB
}

func multiply(numberA: Float, numberB: Float) -> Float {
    return numberA * numberB
}

func divide(numberA: Double, numberB: Double) -> Double {
    return numberA / numberB
}

